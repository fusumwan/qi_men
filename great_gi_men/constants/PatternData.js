/**
 * 依吉凶等級換算分數
 * @param {string} level
 * @param {string} key
 * @returns {number}
 */
function levelToScore(level, key) {
  if (SCORE_OVERRIDES[key] !== undefined) {
    return SCORE_OVERRIDES[key];
  }
  switch (level) {
    case "大吉": return 50;
    case "吉": return 30;
    case "中": return 0;
    case "伏吟": return -20;
    case "凶": return -35;
    case "大凶": return -50;
    default: return 0;
  }
}

/** 建構完整 81 格格局資料 */
function buildPatternData() {
  const data = {};

  STEMS.forEach((tian) => {
    STEMS.forEach((di) => {
      const compactKey = tian + di;
      const key = `${tian}+${di}`;
      const raw = RAW_PATTERNS[compactKey];

      if (!raw) {
        // TODO: 補齊遺漏格局
        return;
      }

      const level = LEVEL_MAP[compactKey] || "中";
      const name = NAME_OVERRIDES[key] || raw.name;

      data[key] = {
        name,
        level,
        score: levelToScore(level, key),
        explanation: raw.explanation
      };
    });
  });

  return data;
}

export const PatternData = buildPatternData();
